namespace Simpler.Net.Text {
	/// <summary>
	/// Controls how characters are trimmed from a string.
	/// </summary>
	public enum SimplerTrimMode {
		/// <summary>
		/// Characters are trimmed from both start and end of the string.
		/// </summary>
		Both,

		/// <summary>
		/// Characters are trimmed from the start of the string.
		/// </summary>
		Start,

		/// <summary>
		/// Characters are trimmed from the end of the string.
		/// </summary>
		End,
	}
}
